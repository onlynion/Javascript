// Index is the position number of array element

const num = [11, 22, 33, 44, 55, 66];

// Array's indexing number always starts from 0

/**
 * In above array:
 * 
 * Length is : 6
 * 
 *  Element of 0 index is : 11
 *  Element of 1 index is : 22
 *  Element of 2 index is : 33 
 *  Element of 3 index is : 44 
 *  Element of 4 index is : 55 
 *  Element of 5 index is : 66
 */

console.log(num); // Accessing full array
console.log(num[0]); // Accessing element of 0 index
console.log(num[1]); // Accessing element of 1 index
console.log(num[2]); // Accessing element of 2 index
console.log(num[3]); // Accessing element of 3 index
console.log(num[4]); // Accessing element of 4 index
console.log(num[5]); // Accessing element of 5 index

// Yo can also store an element in a variable using index

const third = num[2];
console.log(third);
